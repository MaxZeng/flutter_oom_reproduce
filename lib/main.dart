class ImageSizeUtility {
  static Size imageSizeInContext(BuildContext context) {
    // Calculate Thumbnail Size
    final screenWidth = MediaQuery.of(context).size.width;
    final horizontalMargin = 15;
    final thumbnailSpace = 1;
    final imageWidth = (screenWidth - 2 * horizontalMargin - 2 * thumbnailSpace) ~/ 3;
    final imageHeight = imageWidth * 2 ~/ 3;
    return Size(imageWidth.toDouble(), imageHeight.toDouble());
  }
}

